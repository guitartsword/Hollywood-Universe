    public void crearGrafo(String[] Actor1, String[] Actor2, String relacion) {
        Actor temp1 = null;
        Actor temp2 = null;
        try {
            //Busqueda de Existencia
            if (universo.getVertices() != null) {
                for (Actor temp : universo.getVertices()) {
                    if ((Actor1[0] + " " + Actor1[1]).equals(temp.getNombreCompleto())) {
                        temp1 = temp;
                    }
                    if ((Actor2[0] + " " + Actor2[1]).equals(temp.getNombreCompleto())) {
                        temp2 = temp;
                    }
                }
            }
            //Creacion
            if (temp1 == null && temp2 == null) {
                temp1 = new Actor((Actor1[0] + " " + Actor1[1]), Integer.parseInt(Actor1[2]), Actor1[3]);
                historial.push("Se leyo de un Archvio y se creo el Actor " + temp1.toString());
                temp2 = new Actor((Actor2[0] + " " + Actor2[1]), Integer.parseInt(Actor2[2]), Actor2[3]);
                historial.push("Se leyo de un Archvio y se creo el Actor " + temp2.toString());
                if (Actor1.length > 4) {
                    for (int i = 4; i < Actor1.length; i++) {
                        String[] pelicula = Actor1[i].split("-");
                        historial.push("Se leyo de un Archvio y se agrego la pelicula " + pelicula[0] + " al actor " + temp1.toString());
                        temp1.addParticapado(new Pelicula(pelicula[0], Integer.parseInt(pelicula[1]), pelicula[2]));
                    }
                }
                if (Actor2.length > 4) {
                    for (int i = 4; i < Actor2.length; i++) {
                        String[] pelicula = Actor2[i].split("-");
                        historial.push("Se leyo de un Archvio y se agrego la pelicula " + pelicula[0] + " al actor " + temp2.toString());
                        temp2.addParticapado(new Pelicula(pelicula[0], Integer.parseInt(pelicula[1]), pelicula[2]));
                    }
                }

                universo.addVertex(temp1);
                universo.addVertex(temp2);
            } else if (temp1 == null) {
                temp1 = new Actor((Actor1[0] + " " + Actor1[1]), Integer.parseInt(Actor1[2]), Actor1[3]);
                if (Actor1.length > 4) {
                    for (int i = 4; i < Actor1.length; i++) {
                        String[] pelicula = Actor1[i].split("-");
                        historial.push("Se leyo de un Archvio y se agrego la pelicula " + pelicula[0] + " al actor " + temp1.toString());
                        temp1.addParticapado(new Pelicula(pelicula[0], Integer.parseInt(pelicula[1]), pelicula[2]));
                    }
                }
                historial.push("Se leyo de un Archvio y se creo el Actor " + temp1.toString());
                universo.addVertex(temp1);
            } else if (temp2 == null) {
                temp2 = new Actor((Actor2[0] + " " + Actor2[1]), Integer.parseInt(Actor2[2]), Actor2[3]);
                if (Actor2.length > 4) {
                    for (int i = 4; i < Actor2.length; i++) {
                        String[] pelicula = Actor2[i].split("-");
                        historial.push("Se leyo de un Archvio y se agrego la pelicula " + pelicula[0] + " al actor " + temp2.toString());
                        temp2.addParticapado(new Pelicula(pelicula[0], Integer.parseInt(pelicula[1]), pelicula[2]));
                    }
                }
                historial.push("Se leyo de un Archvio y se creo el Actor " + temp2.toString());
                universo.addVertex(temp2);
            }

            if (!temp1.getNombreCompleto().equals(temp2.getNombreCompleto())) {
                boolean pasar = true;
                for (Relaciones temp : universo.getEdges()) {
                    if (temp.getA1().equals(temp1.getNombreCompleto()) && temp.getA2().equals(temp2.getNombreCompleto())) {
                        pasar = false;
                    }
                }
                if (pasar) {
                    historial.push("Se leyo de un Archvio y se agrego la relacion " + relacion + " entre " + temp1.toString() + " y " + temp2.toString());
                    universo.addEdge(new Relaciones(relacion, temp1.getNombreCompleto(), temp2.getNombreCompleto()), temp1, temp2);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al crear el grafo", "ERROR", 2);
        }
    }

    private void dibujarGrafo() {
        Point Center = new Point(menu1.getWidth() / 2, menu1.getHeight() / 2);
        int change = 100;
        int cont = 0;
        boolean diagonal = true;
        menu1.removeAll();
        for (Actor vertex : universo.getVertices()) {
            NodeButton bu = new NodeButton(vertex);
            bu.setContentAreaFilled(false);
            bu.setIcon(new ImageIcon(nodo));
            bu.setSize(nodo.getWidth(), nodo.getHeight());
            if (cont == 0) {
                bu.setLocation(Center);
            } else {
                if (diagonal) {
                    switch (cont % 4) {
                        case 0:
                            bu.setLocation(Center.x + change, Center.y - change);
                            change += 150;
                            diagonal = !diagonal;
                            break;
                        case 1:
                            bu.setLocation(Center.x + change, Center.y + change);
                            break;
                        case 2:
                            bu.setLocation(Center.x - change, Center.y + change);
                            break;
                        case 3:
                            bu.setLocation(Center.x - change, Center.y - change);
                            break;
                    }
                } else {
                    switch (cont % 4) {
                        case 0:
                            bu.setLocation(Center.x, Center.y - change);
                            change += 150;
                            diagonal = !diagonal;
                            break;
                        case 1:
                            bu.setLocation(Center.x + change, Center.y);
                            break;
                        case 2:
                            bu.setLocation(Center.x, Center.y + change);
                            break;
                        case 3:
                            bu.setLocation(Center.x - change, Center.y);
                            break;
                    }
                }
            }
            bu.addActionListener(new java.awt.event.ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    tefi_actorNombreMod.setText(bu.getActor().getNombreCompleto());
                    tefi_actorNacionalidadMod.setText(bu.getActor().getNacionalidad());
                    sp_actorEdadMod.setValue(bu.getActor().getEdad());
                    DefaultListModel<Pelicula> lista = new DefaultListModel();
                    for (Pelicula temp : bu.getActor().getParticapado()) {
                        lista.addElement(temp);
                    }
                    lista_Particiapdo.setModel(lista);
                    vmodificar.pack();
                    vmodificar.setLocationRelativeTo(null);
                    vagregar.setAlwaysOnTop(true);
                    vagregar.setModalityType(Dialog.ModalityType.APPLICATION_MODAL);
                    vmodificar.setVisible(true);
                    SelectedNode = bu;
                }
            });
            bu.addMouseListener(new java.awt.event.MouseAdapter() {
                @Override
                public void mouseEntered(java.awt.event.MouseEvent evt) {
                    DetallesActor.setText("Nombre: " + bu.getActor().toString()
                            + " Edad: " + bu.getActor().getEdad()
                            + " Nacionalidad: " + bu.getActor().getNacionalidad());
                }

                @Override
                public void mouseExited(MouseEvent e) {
                    DetallesActor.setText(null);
                }

            });
            menu1.add(bu);
            cont++;
        }
        
        ArrayList<Line> lines = new ArrayList();
        for(Relaciones arista: universo.getEdges()){
            Pair<Actor> nodes = universo.getEndpoints(arista);
            Actor first = nodes.getFirst();
            Actor second = nodes.getSecond();
            Point start = null;
            Point end = null;
            for(Component node : menu1.getComponents()){
                if(node instanceof NodeButton){
                    if(((NodeButton)node).getActor().equals(first))
                        start = node.getLocation();
                    else if(((NodeButton)node).getActor().equals(second))
                        end = node.getLocation();
                }
            }
            if(start != null && end != null){
                start.translate(15, 15);
                end.translate(15,15);
                lines.add(new Line(start, end));
            }
        }
        ((PanelGrafo)menu1).setLines(lines);
        menu1.repaint();
    }

    private BufferedImage createImage(JComponent component) {
        //  Make sure the component has a size and has been layed out.
        //  (necessary check for components not added to a realized frame)
        Rectangle region = new Rectangle(0, 0, component.getWidth(), component.getHeight());
        if (!component.isDisplayable()) {
            Dimension d = component.getSize();

            if (d.width == 0 || d.height == 0) {
                d = component.getPreferredSize();
                component.setSize(d);
            }

            layoutComponent(component);
        }

        BufferedImage image = new BufferedImage(region.width, region.height, BufferedImage.TYPE_INT_RGB);
        Graphics2D g2d = image.createGraphics();

        //  Paint a background for non-opaque components,
        //  otherwise the background will be black
        if (!component.isOpaque()) {
            g2d.setColor(component.getBackground());
            g2d.fillRect(region.x, region.y, region.width, region.height);
        }

        g2d.translate(-region.x, -region.y);
        component.paint(g2d);
        g2d.dispose();
        return image;
    }
    private void layoutComponent(Component component) {
        synchronized (component.getTreeLock()) {
            component.doLayout();

            if (component instanceof Container) {
                for (Component child : ((Container) component).getComponents()) {
                    layoutComponent(child);
                }
            }
        }
    }
    
